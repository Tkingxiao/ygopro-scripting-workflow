function decodeGBK(buf) {
    let result = '';
    for (let i = 0; i < buf.length; i++) {
        const b = buf[i];
        if (b >= 0x81 && b <= 0xFE) {
            if (i + 1 < buf.length) {
                const b2 = buf[i + 1];
                if ((b2 >= 0x40 && b2 <= 0x7E) || (b2 >= 0x80 && b2 <= 0xFE)) {
                    const gbk = (b << 8) | b2;
                    const unicode = GBK_TO_UNICODE[gbk];
                    if (unicode) {
                        result += String.fromCharCode(unicode);
                    } else {
                        result += '[' + gbk.toString(16) + ']';
                    }
                    i++;
                    continue;
                }
            }
        }
        if (b >= 0x20 && b < 0x7F) {
            result += String.fromCharCode(b);
        } else if (b === 0x0A) {
            result += '\n';
        } else if (b === 0x0D) {
            result += '\r';
        }
    }
    return result;
}

